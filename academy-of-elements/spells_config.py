    """–†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ—Ç —É—Ä–æ–Ω –∑–∞–∫–ª–∏–Ω–∞–Ω–∏—è –Ω–∞ –∑–∞–¥–∞–Ω–Ω–æ–º —É—Ä–æ–≤–Ω–µ."""
    if level < 1:
        return 0
